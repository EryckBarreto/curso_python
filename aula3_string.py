"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica (já sabe qual tipo de informação) / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas

"""
# aspas simples

print('Eryck Barreto')

# aspas duplas
print("Eryck Barreto")

# Escape - para o interpretador pular o caractere seguinte na interpretação
print("Eryck \"Barreto\"")

# r - caso queira que apareça a barra invertida por exemplo
print(r"Eryck \"Barreto\"")

# para o código ficar limpo, melhor usar aspas duplas dentro de aspas simples
# é possível o contrário também, aspas simples dentro de duplas
print(1, 'Eryck "Barreto"')
print(2, "Eryck 'Barreto'")

