# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado
# positivo.
# print(11) # int
# print(-11) # int
# print(0)

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
# print(1.1, 10.11)
# print(0.0, -1.5)

print(10) # int
print(-10) # int

print(2.5) # float
print(0.0, -2.5) # float

# A função (classe) type mostra o tipo que o Python inferiu ao valor

print(type("Eryck"))
print(type(123))
print(type(-1.4))

